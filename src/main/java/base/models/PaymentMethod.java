package base.models;




public class PaymentMethod {
	private int id;
	
	private String paymentType;
	
	
	

	
	

	public PaymentMethod() {
		
	}
	public int getId() {
		return id;
	}


	public void setId(int id) {
		this.id = id;
	}


	public String getPaymentType() {
		return paymentType;
	}


	public void setPaymentType(String paymentType) {
		this.paymentType = paymentType;
	}
}


	
	